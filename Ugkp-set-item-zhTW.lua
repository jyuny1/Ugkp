function Ugkp_ItemSet_Onload()

  itemtypecount = getn(Ugkp_item_auction);
  --for i=1,itemtypecount,1 do
  for i=1,itemtypecount,1 do
    getglobal("UgkpItemSetBorderFrameTab"..i):SetText(Ugkp_item_auction[i]["typename"])
  end
end

function ugkp_change_item_check()

  if(this:GetChecked()) then
    Ugkp_item_auction[ugkp_item_selet_tab]["itemnames"][this:GetID()]["checked"]=1
  else
    Ugkp_item_auction[ugkp_item_selet_tab]["itemnames"][this:GetID()]["checked"]=0
  end
end
